			if data.objectType[i] == objectType then
				local tbl = table.copy(data)
				tbl.TypeName = tid
				tbl.objectType = nil
				return tbl
			end
		end
	end
end

function ItemPropertiesInfo.GetGumpTypeByItemData(id, item)
	if IsMobile(id) then
		return ItemPropertiesInfo.Types.Creature
	end
	local itemData
	for tid, data in pairs(ItemPropertiesInfo.WearableItemsDB) do
		for i=1, #data.objectType do
			if item and data.objectType[i] == item.objectType then
				itemData = table.copy(data)
				break
			end
		end
		if itemData then
			break
		end
	end

	if itemData and itemData.layer then
		if ((itemData.layer == 4 or itemData.layer == 10) and not itemData.isShield)  then -- weapon
			if itemData.isBow then
				return ItemPropertiesInfo.Types.Ranged_Weapon
			elseif itemData.NoDamageWeapon then
				return ItemPropertiesInfo.Types.NoDamageWeapon
			else
				return ItemPropertiesInfo.Types.Weapon
			end
		elseif itemData.layer == 5 or itemData.layer == 9 then -- jewels
			return ItemPropertiesInfo.Types.Jewel
		elseif itemData.isShield then
			return ItemPropertiesInfo.Types.Shield
		else
			return ItemPropertiesInfo.Types.Wearable
		end
	elseif item and item.objectType == 8792 then
		if item.hueId == 1102 then -- smith bod color
			return ItemPropertiesInfo.Types.Bod_Smith
			
		elseif item.hueId == 1155 then -- tailor bod color 
			return ItemPropertiesInfo.Types.Bod_Tailor
			
		else -- unknown item with bod item type id
			return ItemPropertiesInfo.Types.Misc
		end
	else
		return ItemPropertiesInfo.Types.Misc
	end
end

function ItemPropertiesInfo.GetGumpTypeByName(rawData, props)

	-- convert the text into lower case
	rawData = wstring.lower(rawData)

	-- initialize the generic item data
	local itemData

	-- default material
	local material = 0

	local bestMatch

	-- scan the wearables DB
	for tid, data in pairs(ItemPropertiesInfo.WearableItemsDB) do

		-- have we found a match?
		local found = false

		-- get the property text with the same value of the property we're looking for
		local tidString = wstring.lower(GetStringFromTid(tid))

		-- if we don't have a valid string, we can skip to the next
		if not IsValidWString(tidString) then	
			continue
		end

		-- is the current line the same as the tid property we generated?
		if wstring.find(towstring(rawData), tidString, 1, true) then
					
			-- count how many words are matching
			local countMatch = StringUtils.CountMatchingWords(rawData, tidString)  

			-- do we have a best match for the weapon or this match is better?
			if not bestMatch or bestMatch.count < countMatch then

				-- update the best match
				bestMatch = {itemData = table.copy(data), count = countMatch}
			end

			-- if the name is not exactly the same, there could be a special material involved
			if rawData ~= tidString then
				
				-- scan all the materials
				for td, dt in pairs(ItemPropertiesInfo.Materials) do
					
					-- get the material tid name
					local tdText = wstring.lower(GetStringFromTid(td))

					-- are there any tokens inside this material name?
					if CountTokensOnString(tdText) > 0 then

						-- create a testing item name with the material
						tdText = wstring.lower(ReplaceTokens(tdText, {tidString}))
						
						-- is this our item name?
						if rawData == tdText then

							-- get the material name
							material = dt.baseName
							
							-- copy the item data info
							itemData = table.copy(data)

							-- found the right match
							found =  true

							break
						end
					end
				end

				-- have we found a match?
				if found then
					break
				end

			else -- perfect match
				
				-- copy the item data info
				itemData = table.copy(data)

				break
			end
		end
	end

	-- if we haven't found anything, we use the best match we've found
	if bestMatch and not itemData then
		itemData = table.copy(bestMatch.itemData)
	end
	
	-- do we have the item data and a layer?
	if itemData and itemData.layer then

		-- is it a weapon?
		if ((itemData.layer == 4 or itemData.layer == 10) and not itemData.isShield)  then

			-- is it a bow?
			if itemData.isBow then
				return ItemPropertiesInfo.Types.Ranged_Weapon, itemData.objectType[1], material

			-- is it a no damage weapon? (like spellbooks)
			elseif itemData.NoDamageWeapon then
				return ItemPropertiesInfo.Types.NoDamageWeapon, itemData.objectType[1], material

			else -- normal weapon
				return ItemPropertiesInfo.Types.Weapon, itemData.objectType[1], material
			end

		-- is this a jewel?
		elseif itemData.layer == 5 or itemData.layer == 9 then
			return ItemPropertiesInfo.Types.Jewel, itemData.objectType[1], material

		-- is this a shield?
		elseif itemData.isShield then
			return ItemPropertiesInfo.Types.Shield, itemData.objectType[1], material

		else -- generic wearable
			return ItemPropertiesInfo.Types.Wearable, itemData.objectType[1], material
		end

	else -- unknown item

		-- do we have the item properties?
		if props then

			-- do we have weapon damage and weapon speed?
			if props[1061168] and props[1061167] then

				-- do we have weapon range?
				if props[1061169] then
					
					-- ranged weapon
					return ItemPropertiesInfo.Types.Ranged_Weapon, 0
				end
				
				-- normal weapon
				return ItemPropertiesInfo.Types.Weapon, 0
			end
		end
		return ItemPropertiesInfo.Types.Misc
	end
end

