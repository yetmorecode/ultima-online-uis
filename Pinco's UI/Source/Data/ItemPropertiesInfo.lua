function ItemPropertiesInfo.GetLootType(itemName, itemTid, props, itemType, arti, set, worthName)
	if arti then
		return ItemPropertiesInfo.LootOrder.Artifact
	elseif set then
		return ItemPropertiesInfo.LootOrder.Set
	end
	
	-- Finders Keepers system
	if ContainerWindow.FindItems and props then
		for name, data in pairsByKeys(ContainerWindow.FindItems) do
			if data.type then
				local item = FindersKeepers.ItemsList[data.type.name]
				for i=1, #item.objectType do
			
					if itemType == item.objectType[i] then -- correct type
					
						if data.Props then
							local okProps = false
							for propId, dt in pairsByKeys(data.Props) do
								okProps = false
								local tid = dt.tid
								if FindersKeepers.TokenedProperties[tid] then
								
									for ptid, params in pairs(props) do
										if ItemPropertiesInfo.SkillBonusConvert[ptid] then
											local val = tonumber(tostring(params[2]))
											if val >= dt.value then
												okProps = true
											end
											break
										else -- other properties with 2 tokens
											for ptid, params in pairs(props) do
												if tid == ptid then
													local val = tonumber(tostring(params[2]))
													if val >= dt.value then
														okProps = true
													end
													break
												end
											end
										end
									end
									
								elseif not dt.value then -- no tokens properties
								
									for ptid, params in pairs(props) do
										if tid == ptid then
											okProps = true
											break
										end
									end
								
								else -- single tokens properties
									for ptid, params in pairs(props) do
										if tid == ptid then
											local val = tonumber(tostring(params[1]))
											if val >= dt.value then
												okProps = true
											end
											break
										end
									end
								end

								if not okProps then -- if a property doesn't match then the item is not usable
									break
								end
							end

							if okProps then -- properties and type matches, it's a Finders Keepers Item
								if data.trash then
									return ItemPropertiesInfo.LootOrder.Misc, data.hue
								else
									return ItemPropertiesInfo.LootOrder.FindersKeepers, data.hue
								end
							end
						
						else -- type only
							if data.trash then
								return ItemPropertiesInfo.LootOrder.Misc, data.hue
							else
								return ItemPropertiesInfo.LootOrder.FindersKeepers, data.hue
							end
						end
						break
					end
				end
		
			else -- any type items
			
				if data.Props and props then
					local okProps = false
					for propId, dt in pairsByKeys(data.Props) do
						okProps = false
						local tid = dt.tid
					
						if FindersKeepers.TokenedProperties[tid] then
						
							for ptid, params in pairs(props) do
								if ItemPropertiesInfo.SkillBonusConvert[ptid] then
									local val = tonumber(tostring(params[2]))
									if val >= dt.value then
										okProps = true
									end
									break
								else -- other properties with 2 tokens
									for ptid, params in pairs(props) do
										if tid == ptid then
											local val = tonumber(tostring(params[2]))
											if val >= dt.value then
												okProps = true
											end
											break
										end
									end
								end
							end
							
						elseif not dt.value then -- no tokens properties
							for ptid, params in pairs(props) do
								if tid == ptid then
									okProps = true
									break
								end
							end
						
						else -- single tokens properties
						
							for ptid, params in pairs(props) do
								if tid == ptid then
									local val = tonumber(tostring(params[1]))
									if val >= dt.value  then
										okProps = true
									end
									break
								end
							end
						end
						if not okProps then -- if a property doesn't match then the item is not usable
							break
						end
					end
				
					if okProps then -- properties and type matches, it's a Finders Keepers Item
						if data.trash then
							return ItemPropertiesInfo.LootOrder.Misc, data.hue
						else
							return ItemPropertiesInfo.LootOrder.FindersKeepers, data.hue
						end
					end
				end
			
			end
		end
	end
	---
	
	local isMaterial = false
	for tid, types in pairs(ItemPropertiesInfo.MaterialsDB) do
		for i=1, #types do
			if itemType == types[i] then
				isMaterial = true
				break
			end
		end
	end

	if ItemPropertiesInfo.LootMaterials[itemTid] or isMaterial or ItemPropertiesInfo.LootMaterials[itemName] then
		return ItemPropertiesInfo.LootOrder.Materials
	elseif itemType == 3821 or itemType == 3824 or itemTid == 1041361 then -- a bank check
		return ItemPropertiesInfo.LootOrder.Gold
	elseif ItemPropertiesInfo.ResourcesData[itemTid] then
		return ItemPropertiesInfo.LootOrder.Gold
	elseif props and props[1063449] then -- recovered from ~1_val~ (Paragon)
		return ItemPropertiesInfo.LootOrder.ParagonChest
	elseif CourseMapWindow.TmapTID[itemTid] then
		return ItemPropertiesInfo.LootOrder.TreasureMap
	elseif ItemPropertiesInfo.MusicalInstrumentsDB[itemType] then
		return ItemPropertiesInfo.LootOrder.Instruments
	elseif ItemPropertiesInfo.ReagentsDB[itemType] then
		return ItemPropertiesInfo.LootOrder.Reagents
	elseif worthName ~= "" then
		if worthName == "Relic" then
			if props and props[1151488] then -- Minor Magic Item
				return ItemPropertiesInfo.LootOrder.Relic_MinorMagic 
			elseif props and props[1151489] then -- Lesser Magic Item
				return ItemPropertiesInfo.LootOrder.Relic_LesserMagic
			elseif props and props[1151490] then -- Greater Magic Item
				return ItemPropertiesInfo.LootOrder.Relic_GreaterMagic
			elseif props and props[1151491] then -- Major Magic Item
				return ItemPropertiesInfo.LootOrder.Relic_MajorMagic
			elseif props and props[1151492] then -- Lesser Artifact
				return ItemPropertiesInfo.LootOrder.Relic_LesserArtifact
			elseif props and props[1151493] then -- Greater Artifact
				return ItemPropertiesInfo.LootOrder.Relic_GreaterArtifact
			elseif props and props[1151494] then -- Major Artifact
				return ItemPropertiesInfo.LootOrder.Relic_MajorArtifact
			elseif props and props[1151495] then -- Legendary Artifact
				return ItemPropertiesInfo.LootOrder.Relic_LegendaryArtifact
			else
				return ItemPropertiesInfo.LootOrder.Relic
			end
		elseif worthName == "Essence" then
			if props and props[1151488] then -- Minor Magic Item
				return ItemPropertiesInfo.LootOrder.Essence_MinorMagic 
			elseif props and props[1151489] then -- Lesser Magic Item
				return ItemPropertiesInfo.LootOrder.Essence_LesserMagic
			elseif props and props[1151490] then -- Greater Magic Item
				return ItemPropertiesInfo.LootOrder.Essence_GreaterMagic
			elseif props and props[1151491] then -- Major Magic Item
				return ItemPropertiesInfo.LootOrder.Essence_MajorMagic
			elseif props and props[1151492] then -- Lesser Artifact
				return ItemPropertiesInfo.LootOrder.Essence_LesserArtifact
			elseif props and props[1151493] then -- Greater Artifact
				return ItemPropertiesInfo.LootOrder.Essence_GreaterArtifact
			elseif props and props[1151494] then -- Major Artifact
				return ItemPropertiesInfo.LootOrder.Essence_MajorArtifact
			elseif props and props[1151495] then -- Legendary Artifact
				return ItemPropertiesInfo.LootOrder.Essence_LegendaryArtifact
			else
				return ItemPropertiesInfo.LootOrder.Essence
			end
		elseif worthName == "Residue" then
			if props and props[1151488] then -- Minor Magic Item
				return ItemPropertiesInfo.LootOrder.Residue_MinorMagic 
			elseif props and props[1151489] then -- Lesser Magic Item
				return ItemPropertiesInfo.LootOrder.Residue_LesserMagic
			elseif props and props[1151490] then -- Greater Magic Item
				return ItemPropertiesInfo.LootOrder.Residue_GreaterMagic
			elseif props and props[1151491] then -- Major Magic Item
				return ItemPropertiesInfo.LootOrder.Residue_MajorMagic
			elseif props and props[1151492] then -- Lesser Artifact
				return ItemPropertiesInfo.LootOrder.Residue_LesserArtifact
			elseif props and props[1151493] then -- Greater Artifact
				return ItemPropertiesInfo.LootOrder.Residue_GreaterArtifact
			elseif props and props[1151494] then -- Major Artifact
				return ItemPropertiesInfo.LootOrder.Residue_MajorArtifact
			elseif props and props[1151495] then -- Legendary Artifact
				return ItemPropertiesInfo.LootOrder.Residue_LegendaryArtifact
			else
				return ItemPropertiesInfo.LootOrder.Residue
			end
		end
	end
	return ItemPropertiesInfo.LootOrder.Misc
end

