function ItemPropertiesInfo.IsMedable(objectType)
	for tid, data in pairs(ItemPropertiesInfo.WearableItemsDB) do
		for i=1, #data.objectType do
			if data.objectType[i] == objectType then
				if data.medable ~= nil then
					return data.medable
				end
				if data.isJewel ~= nil then
					return data.isJewel
				end
			end
		end
	end
	return false
end

